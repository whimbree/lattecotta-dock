//! EX-10 MaskInputGeometry (docs/tracking/QML_EXTRACTION_PLAN.md): the visibility
//! mask (local geometry) and input-region rect tables extracted from
//! VisibilityManager.qml's updateMaskArea/updateInputGeometry pair.
//!
//! Expected values are hand-derived from the per-edge formulas of the QML
//! bodies at the extraction commit (the local-geometry half matches
//! f0ad7b23 verbatim, the input half matches upstream 11f42978), never
//! generated by running the code under test.
//!
//! The shared fixture, used by most rows: a bottom-edge dock in a
//! 1200x140 view window (root item fills it), applets spanning
//! effects.rect x=200 width=800, clean thickness 56, floating screen-edge
//! gap 12. Vertical rows transpose it to 140x800 with effects.rect
//! y=100 height=600.

namespace QTest {
//! decision diagnostics for QCOMPARE failures
template<>
char *toString(const InputMaskDecision &decision)
{
    return std::visit([](const auto &alternative) {
        using T = std::decay_t<decltype(alternative)>;
        if constexpr (std::is_same_v<T, AcceptAllInput>) {
            return qstrdup("AcceptAllInput");
        } else if constexpr (std::is_same_v<T, AcceptInputWithin>) {
            return qstrdup(qPrintable(QStringLiteral("AcceptInputWithin(%1, %2, %3x%4)")
                                              .arg(alternative.rect.x())
                                              .arg(alternative.rect.y())
                                              .arg(alternative.rect.width())
                                              .arg(alternative.rect.height())));
        } else {
            static_assert(std::is_same_v<T, AcceptNoInput>);
            return qstrdup("AcceptNoInput");
        }
    }, decision);
}
}

