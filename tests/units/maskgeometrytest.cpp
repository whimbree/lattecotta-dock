void MaskGeometryTest::localGeometryWarmupZeroSizePassesThrough()
{
    //! the documented warmup protocol (app/view/effects.cpp:330): while
    //! the layouter warms up, zero sizes legitimately flow through and the
    //! degenerate result makes Effects clear the window mask. This must
    //! NOT be refused or "fixed" upward - a loud refusal would fire on
    //! every startup for a legitimate state.
    MaskGeometry::LocalGeometryInputs in;
    in.location = Plasma::Types::BottomEdge;
    in.behaveAsPlasmaPanel = false;
    in.rootSize = QSizeF(0, 0);
    in.viewSize = QSize(0, 0);
    in.effectsRect = QRect(0, 0, 0, 0);
    in.totalsThickness = 56;
    in.screenEdgeMargin = 0;

    const QRectF result = MaskGeometry::computeLocalGeometry(in);
    QCOMPARE(result, QRectF(0, 0, 0, 0));
    QVERIFY(result.isEmpty());
}

void MaskGeometryTest::inputMaskPerEdgeAndState_data()
{
    QTest::addColumn<int>("location");
    QTest::addColumn<bool>("vertical");
    QTest::addColumn<bool>("isHidden");
    QTest::addColumn<bool>("parabolicAnimating");
    QTest::addColumn<InputMaskDecision>("expected");

    //! normal state: thickness = mask.screenEdge 12 + totals.thickness 56
    //! = 68, length axis from localGeometry
    QTest::newRow("top normal") << int(Plasma::Types::TopEdge) << false << false << false
                                << InputMaskDecision(AcceptInputWithin{QRectF(200, 0, 800, 68)});
    QTest::newRow("bottom normal") << int(Plasma::Types::BottomEdge) << false << false << false
                                   << InputMaskDecision(AcceptInputWithin{QRectF(200, 140 - 68, 800, 68)});
    QTest::newRow("left normal") << int(Plasma::Types::LeftEdge) << true << false << false
                                 << InputMaskDecision(AcceptInputWithin{QRectF(0, 100, 68, 600)});
    QTest::newRow("right normal") << int(Plasma::Types::RightEdge) << true << false << false
                                  << InputMaskDecision(AcceptInputWithin{QRectF(140 - 68, 100, 68, 600)});

    //! hidden: a 2px reveal strip on the screen edge, length axis still
    //! from localGeometry
    QTest::newRow("top hidden") << int(Plasma::Types::TopEdge) << false << true << false
                                << InputMaskDecision(AcceptInputWithin{QRectF(200, 0, 800, 2)});
    QTest::newRow("bottom hidden") << int(Plasma::Types::BottomEdge) << false << true << false
                                   << InputMaskDecision(AcceptInputWithin{QRectF(200, 138, 800, 2)});
    QTest::newRow("left hidden") << int(Plasma::Types::LeftEdge) << true << true << false
                                 << InputMaskDecision(AcceptInputWithin{QRectF(0, 100, 2, 600)});
    QTest::newRow("right hidden") << int(Plasma::Types::RightEdge) << true << true << false
                                  << InputMaskDecision(AcceptInputWithin{QRectF(138, 100, 2, 600)});

    //! parabolic zoom in flight (upstream 11f42978): the length axis
    //! widens to the full root span (localGeometry is NOT read) and the
    //! thickness grows to mask.thickness.zoomedForItems 102, so the
    //! zoomed item's outer pixels keep receiving input instead of
    //! flapping enter/exit
    QTest::newRow("top animated") << int(Plasma::Types::TopEdge) << false << false << true
                                  << InputMaskDecision(AcceptInputWithin{QRectF(0, 0, 1200, 102)});
    QTest::newRow("bottom animated") << int(Plasma::Types::BottomEdge) << false << false << true
                                     << InputMaskDecision(AcceptInputWithin{QRectF(0, 140 - 102, 1200, 102)});
    QTest::newRow("left animated") << int(Plasma::Types::LeftEdge) << true << false << true
                                   << InputMaskDecision(AcceptInputWithin{QRectF(0, 0, 102, 800)});
    QTest::newRow("right animated") << int(Plasma::Types::RightEdge) << true << false << true
                                    << InputMaskDecision(AcceptInputWithin{QRectF(140 - 102, 0, 102, 800)});
}

