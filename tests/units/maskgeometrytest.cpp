MaskGeometry::LocalGeometryInputs horizontalLocalInputs(Plasma::Types::Location location)
{
    MaskGeometry::LocalGeometryInputs in;
    in.location = location;
    in.behaveAsPlasmaPanel = false;
    in.rootSize = QSizeF(1200, 140);
    in.viewSize = QSize(1200, 140);
    in.effectsRect = QRect(200, 0, 800, 140);
    in.totalsThickness = 56;
    in.screenEdgeMargin = 12;
    return in;
}

MaskGeometry::LocalGeometryInputs verticalLocalInputs(Plasma::Types::Location location)
{
    MaskGeometry::LocalGeometryInputs in;
    in.location = location;
    in.behaveAsPlasmaPanel = false;
    in.rootSize = QSizeF(140, 800);
    in.viewSize = QSize(140, 800);
    in.effectsRect = QRect(0, 100, 140, 600);
    in.totalsThickness = 56;
    in.screenEdgeMargin = 12;
    return in;
}

MaskGeometry::InputMaskInputs horizontalInputMaskInputs(Plasma::Types::Location location)
{
    MaskGeometry::InputMaskInputs in;
    in.location = location;
    in.compositingActive = true;
    in.behaveAsPlasmaPanel = false;
    in.isHidden = false;
    in.isSidebar = false;
    in.parabolicAnimating = false;
    in.floatingGapInputDisabled = false;
    in.hiddenThickness = 2;
    in.zoomedForItemsThickness = 102;
    in.itemsScreenEdgeMargin = 12;
    in.totalsThickness = 56;
    in.maskScreenEdgeMargin = 12;
    in.localGeometry = QRect(200, 72, 800, 56); //! the bottom-edge local geometry
    in.rootSize = QSizeF(1200, 140);
    in.viewSize = QSize(1200, 140);
    return in;
}

MaskGeometry::InputMaskInputs verticalInputMaskInputs(Plasma::Types::Location location)
{
    MaskGeometry::InputMaskInputs in = horizontalInputMaskInputs(location);
    in.localGeometry = QRect(12, 100, 56, 600); //! the left-edge local geometry
    in.rootSize = QSizeF(140, 800);
    in.viewSize = QSize(140, 800);
    return in;
}

