void DropClassifierTest::tasksDropLadder_truthTable()
{
    using Action = DropClassifier::TasksDropAction;

    QCOMPARE(DropClassifier::tasksDropAction({}), Action::Ignore);
    QCOMPARE(DropClassifier::tasksDropAction({.droppingSeparator = true}), Action::AddSeparator);
    //! separator wins over files when a drag carries both (reachable:
    //! a separator payload that also has non-application urls)
    QCOMPARE(DropClassifier::tasksDropAction({.droppingSeparator = true, .droppingFiles = true}), Action::AddSeparator);
    QCOMPARE(DropClassifier::tasksDropAction({.droppingOnlyLaunchers = true}), Action::DropUrls);
    QCOMPARE(DropClassifier::tasksDropAction({.droppingFiles = true}), Action::DropUrls);
    //! an internal task move released over the area: accepted at enter,
    //! deliberately does nothing at drop ("Reject internal drops")
    QCOMPARE(DropClassifier::tasksDropAction({.movingTask = true}), Action::LeaveUnchanged);
    //! movingTask + files (a task drag carrying urls): the files route
    //! fires, because the Qt5 else-if chain checked separator/launchers/
    //! files before falling through to the internal-move no-op
    QCOMPARE(DropClassifier::tasksDropAction({.movingTask = true, .droppingFiles = true}), Action::DropUrls);

    QVERIFY(!DropClassifier::tasksDragAccepts({}));
    QVERIFY(DropClassifier::tasksDragAccepts({.movingTask = true}));
    QVERIFY(DropClassifier::tasksDragAccepts({.droppingFiles = true}));
}

