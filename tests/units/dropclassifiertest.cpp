void DropClassifierTest::containmentEnterLadder_truthTable()
{
    using Action = DropClassifier::ContainmentEnterAction;
    const DropClassifier::ContainmentDragFlags launchers{.onlyLaunchers = true};
    const DropClassifier::ContainmentDragFlags task{.isTask = true, .onlyLaunchers = true};
    const DropClassifier::ContainmentDragFlags plain{};

    //! rejection outranks everything, launchers-stealing outranks spacer
    QCOMPARE(DropClassifier::containmentDragEnterAction(task, false, true, true), Action::Reject);
    QCOMPARE(DropClassifier::containmentDragEnterAction(plain, true, true, false), Action::Reject);
    QCOMPARE(DropClassifier::containmentDragEnterAction(plain, false, false, false), Action::Reject);
    QCOMPARE(DropClassifier::containmentDragEnterAction(launchers, false, true, true), Action::ShowAddLaunchersMessage);
    //! launchers without a stealing applet route to the plain spacer
    QCOMPARE(DropClassifier::containmentDragEnterAction(launchers, false, true, false), Action::InsertSpacer);
    QCOMPARE(DropClassifier::containmentDragEnterAction(plain, false, true, true), Action::InsertSpacer);
}

