// EX-18 LengthOffsetClamp (docs/tracking/QML_EXTRACTION_PLAN.md): the mutual
// maxLength/minLength/offset clamp that keeps a dock on-screen, dedup'd
// out of the two Qt5-inherited QML copies (the canvas maxLength ruler and
// the Appearance page's slider cluster). Divergence between those copies
// was a config-corruption class: both edit the same three config keys.
//
// Expected slider-specific values are hand-derived from the shipped QML bodies
// at the extraction commit (verified byte-identical in math to their f0ad7b23
// Qt5 ancestors; docs/agent-logs/EX-18.md has the arbitration record). The
// final placement transaction deliberately replaces Qt5's alignment-change
// bypass and out-of-domain multi-step healing; placementstate.h records both
// the root invariant and the deviation. No row was generated by running the
// header under test.
//
// The one deliberate deviation (recorded in the spec and the agent log):
// the page slider's off-screen correction runs on the EFFECTIVE (floored)
// maxLength where Qt5 used the raw slider value; the two disagree only in
// a state the slider wiring makes unreachable, and the raw form breaks
// the on-screen guarantee and idempotence the spec's test plan demands.
// toValue_correctionUsesEffectiveMaxLength pins the fixed behavior with
// the counterexample.

