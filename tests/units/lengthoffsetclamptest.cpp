void LengthOffsetClampTest::byStep_couplingWalksBothRailsDown()
{
    // caught on paper while reading the ruler body: an uncoupled pair
    // becomes coupled the moment the min floor catches the wheel, and
    // from then on both rails walk down together to the 30 floor
    LengthState state{45.0, 40.0, 0.0};

    state = clampMaxLengthByStep(state, -6.0, Alignment::Edge);
    QCOMPARE(state.maxLength, 40.0);   // floor caught it, now coupled
    QCOMPARE(state.minLength, 40.0);

    state = clampMaxLengthByStep(state, -6.0, Alignment::Edge);
    QCOMPARE(state.maxLength, 34.0);   // coupled walk
    QCOMPARE(state.minLength, 34.0);

    state = clampMaxLengthByStep(state, -6.0, Alignment::Edge);
    QCOMPARE(state.maxLength, 30.0);   // both stop on the rail
    QCOMPARE(state.minLength, 30.0);

    state = clampMaxLengthByStep(state, -6.0, Alignment::Edge);
    QCOMPARE(state.maxLength, 30.0);   // and stay there
    QCOMPARE(state.minLength, 30.0);
}

