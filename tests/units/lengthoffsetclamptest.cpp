void LengthOffsetClampTest::bridge_refusesNonFiniteLoudly()
{
    // the boundary refusal (qCritical-and-refuse; the router wrapper is
    // the model): KConfig parses "nan" into a double, and the Qt5 QML
    // silently pushed it back into the config. The bridge refuses the
    // whole call and returns the input unchanged - no partial clamping
    // of a corrupt state, and the message names the fix.
    Latte::Settings::LengthOffsetClampBridge bridge;
    const QRegularExpression refusal(QStringLiteral("refused: non-finite"));
    const double nan = std::nan("");
    const double inf = std::numeric_limits<double>::infinity();

    QTest::ignoreMessage(QtCriticalMsg, refusal);
    const QVariantMap stepped = bridge.clampMaxLengthByStep(nan, 30.0, 0.0, 6.0, 0);
    QVERIFY(std::isnan(stepped.value(QStringLiteral("maxLength")).toDouble()));
    QCOMPARE(stepped.value(QStringLiteral("minLength")).toDouble(), 30.0);
    QCOMPARE(stepped.value(QStringLiteral("offset")).toDouble(), 0.0);

    QTest::ignoreMessage(QtCriticalMsg, refusal);
    const QVariantMap valued = bridge.clampMaxLengthToValue(50.0, inf, 0.0, 0);
    QVERIFY(std::isinf(valued.value(QStringLiteral("minLength")).toDouble()));

    QTest::ignoreMessage(QtCriticalMsg, refusal);
    const QVariantMap offsetMap = bridge.clampOffset(80.0, nan, 0);
    QCOMPARE(offsetMap.value(QStringLiteral("maxLength")).toDouble(), 80.0);
    QVERIFY(std::isnan(offsetMap.value(QStringLiteral("offset")).toDouble()));

    QTest::ignoreMessage(QtCriticalMsg, refusal);
    // bounds refusal collapses to 0..0: the page disables the offset row
    // when to <= from, so a corrupt maxLength shows as a disabled control
    // plus the log line, never as NaN bounds propagating into the slider
    QCOMPARE(bridge.offsetSliderFrom(nan, 0), 0.0);
    QTest::ignoreMessage(QtCriticalMsg, refusal);
    QCOMPARE(bridge.offsetSliderTo(nan, 0), 0.0);
}

