void LengthOffsetClampTest::crossedUpdates_convergeOnScreenWithMinBelowMax()
{
    // spec slot: min<=max under crossed updates. The Appearance page's
    // wiring after a minLength raise is: minLength is written as-is (no
    // math), then the max slider's update refloors maxLength, which may
    // spill into the offset correction. Replayed through the core:
    LengthState state{90.0, 30.0, 0.0};

    const OffsetClamp afterOffset = clampOffset(state.maxLength, 5.0, Alignment::Centered);
    state.maxLength = afterOffset.maxLength;
    state.offset = afterOffset.offset;
    QCOMPARE(state.maxLength, 90.0);
    QCOMPARE(state.offset, 5.0);

    state.minLength = 95.0;   // the min slider writes freely, crossing max

    const LengthState refloored = clampMaxLengthToValue(state.maxLength, state.minLength,
                                                        state.offset, Alignment::Centered);
    QCOMPARE(refloored.maxLength, 95.0);   // pulled up to min
    QCOMPARE(refloored.offset, 2.5);       // spill recentered: 50 - 95/2
    QVERIFY(refloored.minLength <= refloored.maxLength);
    QVERIFY(staysOnScreen(refloored.maxLength, refloored.offset, Alignment::Centered));
}

void LengthOffsetClampTest::rulerThenSlider_truncationDisagreementIsPinned()
{
    // inherited Qt5 cross-copy seam, pinned as documentation: the ruler's
    // correction can write a fractional offset (50 - 79/2 = 10.5), which
    // the offset slider's truncated int bounds then pull to 10 the next
    // time its update runs. Both copies did exactly this in Qt5; the core
    // preserves it (the truncation IS the int-property conversion).
    const LengthState ruled = clampMaxLengthByStep({73.0, 30.0, 40.0}, 6.0, Alignment::Centered);
    QCOMPARE(ruled.maxLength, 79.0);
    QCOMPARE(ruled.offset, 10.5);

    const OffsetClamp slid = clampOffset(ruled.maxLength, ruled.offset, Alignment::Centered);
    QCOMPARE(slid.maxLength, 79.0);
    QCOMPARE(slid.offset, 10.0);
}

void LengthOffsetClampTest::bridge_mapsAlignmentTotally()
{
    // Latte::Types::Alignment: Center=0 and Justify=10 take the centered
    // branches; every other int (edges, NoneAlignment=-1, garbage from a
    // hand-edited config) behaves as an edge alignment - the same total
    // predicate the shipped QML applied with ===
    Latte::Settings::LengthOffsetClampBridge bridge;

    const struct { int alignment; bool centered; } rows[] = {
        {0, true},    // Center
        {10, true},   // Justify
        {1, false},   // Left
        {2, false},   // Right
        {3, false},   // Top
        {4, false},   // Bottom
        {-1, false},  // NoneAlignment
        {99, false},  // out-of-domain
    };
    for (const auto &row : rows) {
        const double from = bridge.offsetSliderFrom(0.0, row.alignment);
        QCOMPARE(from, row.centered ? -50.0 : 0.0);
        const double to = bridge.offsetSliderTo(0.0, row.alignment);
        QCOMPARE(to, row.centered ? 50.0 : 100.0);
    }
}

void LengthOffsetClampTest::bridge_mapsJustifyToItsOwnFloor()
{
    // D17 through the QML boundary: Justify (Latte::Types::Justify == 10)
    // honours a Maximum below the stored minimum; Center (0) and the edges
    // (1..4) still floor. The geometry stays shared - Justify's offset bounds
    // remain the centered bounds (bridge_mapsAlignmentTotally pins that half).
    Latte::Settings::LengthOffsetClampBridge bridge;

    const QVariantMap justify = bridge.clampMaxLengthToValue(40.0, 50.0, 0.0, 10);
    QCOMPARE(justify.value(QStringLiteral("maxLength")).toDouble(), 40.0);
    QCOMPARE(justify.value(QStringLiteral("minLength")).toDouble(), 50.0);

    for (const int floored : {0, 1, 2, 3, 4}) {   // Center, Left, Right, Top, Bottom
        const QVariantMap map = bridge.clampMaxLengthToValue(40.0, 50.0, 0.0, floored);
        QCOMPARE(map.value(QStringLiteral("maxLength")).toDouble(), 50.0);
    }
}

